"""
集合添加元素的方法：
    1、add()
        往集合中添加元素  数字、字符串、元组
    2、update()
        可以将集合进行合并
"""
a = {1,2,3}
a.add(6)
print(a)    #{1, 2, 3, 6} 集合中不能添加重复的元素
a.add("dcdad")
print(a)    #{1, 2, 3, 6, 'dcdad'}
a.add((4,5,6,8))
print(a)    #{1, 2, 3, 'dcdad', 6, (4, 5, 6, 8)}  集合是无序随机排列的，不重复的

#集合进行合并
b = {1,4,7}
c = {"w","e","r"}
b.update(c)     #{1, 'w', 'e', 4, 7, 'r'}
print(b)